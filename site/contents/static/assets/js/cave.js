function draw_man(x, y, angle, direction, walk_pos) {
    stroke(0, 0, 0);
    strokeWeight(0.5);
    fill(0, 0, 0);
    pushMatrix();
    translate(x, y);
    scale(direction*1, 1);
    rotate(radians(direction*angle));
    ellipse(1, -5, 3, 3);
    line(0, -5, 0, 1);
    // arms
    line(0, -3, 3, -1);
    line(0, -3, -1, 0);
    // legs
    line(0, 0, 2, 3);
    line(0, 0, -2, 3);
    popMatrix();
}


function createMan() {
    var direction = 1;
    var walk_pos = 0;
    var current_point = 0;
    var o = {
      point: function() {
        return current_point;
      },
      render: function() {
        var p = data.point(current_point);
        var angle = data.angle(current_point);
        draw_man(p.x, p.y, angle, direction, walk_pos);
        current_point = current_point + direction*2;
        if (current_point >= data.point_count() ||
          current_point === 0) {
          direction *= -1;
        }
        walk_pos = (walk_pos + 1) % 2;

      }
    };
    return o;
}


